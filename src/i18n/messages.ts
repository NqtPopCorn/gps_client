const defaultCatalog = baseMessages;

const catalogs = (Object.keys(LANGUAGES) as LangCode[]).reduce(
  (acc, lang) => {
    acc[lang] = {
      ...defaultCatalog,
      ...(overrides[lang] ?? {}),
    };
    return acc;
  },
  {} as Record<LangCode, Record<BaseKey, string>>,
);

export type TranslationKey = BaseKey;

const FALLBACK_LANG: LangCode = "en";

const PLACEHOLDER_REGEX = /{{(.*?)}}/g;

export function translate(
  lang: LangCode,
  key: TranslationKey,
  vars?: Record<string, string | number>,
) {
  const catalog = catalogs[lang] ?? catalogs[FALLBACK_LANG];
  const template = catalog[key] ?? catalogs[FALLBACK_LANG][key] ?? key;
  if (!vars) return template;
  return template.replace(PLACEHOLDER_REGEX, (_, token: string) => {
    const value = vars[token.trim()];
    return value !== undefined ? String(value) : "";
  });
}

export function getCatalog(lang: LangCode) {
  return catalogs[lang] ?? catalogs[FALLBACK_LANG];
}
